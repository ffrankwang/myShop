package com.frank.shop.user.action;

import java.io.IOException;

import javax.servlet.http.HttpServletResponse;

import org.apache.struts2.ServletActionContext;

import com.frank.shop.user.domain.User;
import com.frank.shop.user.service.UserService;
import com.opensymphony.xwork2.ActionSupport;
import com.opensymphony.xwork2.ModelDriven;

/**
 * @author mstig 用户模块的Action
 */
public class UserAction extends ActionSupport implements ModelDriven<User> {
	// 模型驱动使用的对象
	private User user = new User();

	public User getModel() {
		return user;
	}

	private String checkcode;

	/* 接收验证码 */
	public void setCheckcode(String checkcode) {
		this.checkcode = checkcode;
	}

	/**
	 * 跳转到注册页面的执行方法
	 */
	public String registPage() {
		return "registPage";
	}

	// 注入UserService
	private UserService userService;

	public void setUserService(UserService userService) {
		this.userService = userService;
	}

	/**
	 * AJAX进行异步校验用户名的执行方法
	 * 
	 * @throws IOException
	 */
	public String findByUsername() throws IOException {
		// 调用Service进行查询:
		User existUser = userService.findByUsername(user.getUsername());
		// 获得response对象,项页面输出:
		HttpServletResponse response = ServletActionContext.getResponse();
		response.setContentType("text/html;charset=UTF-8");
		// 判断
		if (existUser != null) {
			// 查询到该用户:用户名已经存在
			response.getWriter().println("<font color='red'>用户名已经存在</font>");
		} else {
			// 没查询到该用户:用户名可以使用
			response.getWriter().println("<font color='green'>用户名可以使用</font>");
		}
		return NONE;
	}

	/**
	 * 用户注册方法
	 */
	public String regist() {
		// 判断验证码程序:
				// 从session中获得验证码的随机值:
				String checkcode1 = (String) ServletActionContext.getRequest()
						.getSession().getAttribute("checkcode");
				if(!checkcode.equalsIgnoreCase(checkcode1)){
					this.addActionError("验证码输入错误!");
					return "checkcodeFail";
				}
		userService.save(user);
		this.addActionMessage("注册成功！请去邮箱激活。");
		return "msg";
	}

	/**
	 * 用户激活的方法
	 * 
	 */
	public String active() {
		User existUser = userService.findByCode(user.getCode());
		if (existUser == null) {
			this.addActionMessage("激活失败，激活码错误！");
		} else {
			userService.update(existUser);
			this.addActionMessage("激活成功，请去首页登陆。");
		}
		return "msg";
	}

	/**
	 * 跳转到用户登陆界面的方法
	 * 
	 * @return
	 */
	public String loginPage() {
		return "loginPage";
	}

	/**
	 * 用户登陆方法
	 * 
	 * @return
	 */
	public String login() {
		User existUser = userService.login(user);
		if (existUser != null) {
			// 登录成功
			ServletActionContext.getRequest().getSession()
					.setAttribute("existUser", existUser);
			return "loginSuccess";
		} else {
			// 登录失败
			this.addActionError("登陆失败，用户名或密码错误或用户未激活。");
			return "loginPage";
		}
	}

	/**
	 * 用户退出的方法
	 * 
	 * @return
	 */
	public String logout() {
		// 销毁session
		ServletActionContext.getRequest().getSession().invalidate();
		return "logout";
	}

}
